Lila y la quiniela.

sep 

Hoy, 
Lila había ido
a la Agencia de Quiniela
que se halla en misma cuadra.

Cuando vuelve, 
con una sonrisa
le pregunto:

¿Qué número jugaste?

Me dice:

«¡No te voy a decir!»

E inmediatamente
me dice 

«Jugué el 32»

Le digo, 
por haberte contenido, 
por haber sido respetuosa, 
por haber respondido
con cortesía, 

el número que jugaste
va a salir.

sep

Se quedó feliz de la vida.

¡Sabe que mi palabra es!

sep

¡Y así fue!

sep

Yosoy, 
vivo aquí, 
estoy entre ustedes!

Y ustedes, 
me niegan, 
se ríen, 
se burlan, 

y luego 
me pretenden
en vuestras horas aciagas.

sep

Porque si por vuestras
acciones, 
se alejan de mí, 
se acercan 
a aquellos adoradores
de la violencia, 
del fuego, 
del infierno, 
y ellos los arrastran consigo
a ustedes y los suyos.

sep

Los arrastran consigo, 
esos demonios 
los llevan a su mundo, 
porque ustedes
demuestran adorarlos
y vuestro gusto.

sep

Lo mío es amor, 
es paz, 
es misericordia, 
es paciencia, 
y los que son conmigo, 
están bajo mi protección,
y el mal les huye.

