¡Vuestro engaño!

sep

Lo producen ustedes mismos.

sep

No miren a Luis.

Luis es uno más
igual que ustedes.

Luis, 
podría ser un niño,
podría ser un abuelo, 
podría ser una madre, 
un recién nacido.

Luis, 
no tiene nada de mí
más de lo que todos tienen.

No Puede decirle al viento
que pare.
A las nubes que descarguen.

Igual que ustedes
lleva su vida adelante
con esfuerzo, sacrificio
y en silencio.

No lo he visto 
nunca quejarse.

Sí, lo he visto amargado
con vuestra falta de amor.

sep

Luis es mi escribiente.

Solo eso.

Siempre está dispuesto, 
a la hora que lo llamo.

sep

Nunca lo he visto titubear
ni cuestionar
por temor a su responsabilidad
ante ustedes.

Sí, lo he visto lagrimear
cuando le dicto.

sep

Nunca le he corregido
una falta de ortografía
porque para mí 
las faltas de ortografías

no se escriben
se llevan adelante
con falta de amor.

sep

Vuestro engaño
es mirarlo a Luis, 

y allí pierden
todo amor y respeto
por la palabra de vuestro Padre.

sep

Vuestro engaño
es oír
a quién los guía
para vuestro mal.

sep

A veces esa voz
es interior
y otras veces
aparecen seres 
totalmente sumidos por el mal
y los seducen 
con las palabras
que ustedes esperan oír.

sep

Porque el mal
les hablará
solo si vuestra alma
está dispuesta, 

si no tratará 
de predisponerlos.

sep

¡Vuestro engaño!
¡Es vuestro mal!

No miren a Luis.

Solo presten atención
a La Palabra.

