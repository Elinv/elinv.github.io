¡El Peor Pecado!

sep

Les va a significar

aquel 
del que nunca 
tomaron conocimiento.

Vuestro orgullo, 
vuestro amor propio, 
esos dientes comprados, 
ese pelo adquirido
cuidado, mimado, 
más que a vuestra propia familia.

A mis 17 años, 
en un Instituto Policial, 
un peluquero, 
me dejo la cabeza 
como mi rodilla.

sep

Nunca más 
acepté dejarme
crecer el pelo.

En aquel momento
nada sentí.

Realmente me ha significado
muy cómodo para vivir
me ha simplificado
el conocer a la mayoría.

Solo un poco de frío
por las noches
y calor durante el día
pero nos facilitaron 
un quepi.

sep

Hoy, 
yo mismo me paso 
una máquina 
con el número 1.

sep

¿Saben por qué? 
soy pelado?

sep

No lo saben…
Podrán oír, 
leer mis palabras

pero no lo saben.

sep

Deberían saber 
del cuidado 
que deben tener 
al hablar, 
al pensar, 
al burlarse
o reírse del otro, 

porque le faltan sus dientes, 
o porque no tiene cabello
o por vuestra tanta superficialidad
propia de vuestra
alma empobrecida.

sep

Porque les demandaré…

el daño que causaron 
en las almas, 
con vuestra insensibilidad
con vuestra necesidad
de superioridad
de reírse 
a costillas del prójimo.

sep

¡Dicen Dios mío!

Y les digo, 
no soy vuestro.

Si fuera vuestro, 
sabrían del dolor
que me causan 
cuando se burlan,
 
aun sin saber
del daño que causan
a sus hermanos, 
que no están tan bien
como ustedes.

sep

En estos días he visto
a una hija mía, 
irse.

Luego de pelear 
un día más de vida
se fue 
habiendo perdido 
todo su pelo.

Ella, 
se mostraba ante mí, 
tan bella, 
sonriente, 
fuerte, altiva, 
y cada vez 
vi en ella mi amor.

Ella está ante mí
ella y su alma bondadosa
su alma amorosa, 
su alma fuerte, 
su alma
que ya es parte mía.

sep

Ella nunca fue 
lo que el espejo, 

tampoco lo que los demás, 

ella fue 
y siempre será 
mi amor entre ustedes.

Un amor
que muchos de ustedes
apreciaron 
y otros tantos no.

Hoy, 
ella es mi amor, 
a mi lado.

Y gracias a ella, 
todos los suyos
serán con ella.

sep

Guarden sus calificaciones
y no tendrán
de que arrepentirse.

Refrenen vuestras bocas
y no hagan 
alarde de una inteligencia
que no tienen
porque eso es la necedad.

sep

Conocen mi amor, 
conocen mi justicia, 
conocen todo lo mío, 
y mi palabra
es para que todos
lleguen a mí.

