Un joven ante mí.

sep

Vino ante mí, 
me expresó su deseo
de conseguir un préstamo
quería concretar el sueño
de la casa propia.

Me dijo que lo solicitaría.

«Hazlo, fue mi palabra.»

sep

No salió elegible
y no se lo dieron.

sep

Tiempo después
volvieron a salir
este tipo de préstamos.

El joven me dijo,
 
«me volví a anotar
pero es de gusto
no me lo darán…»

¡Bien hiciste! Le dije.

sep

No salió elegible
y su disgusto no fue tanto
porque en su interior
ya lo sabía.

sep

Mucho tiempo después
volvieron a salir
esos préstamos, 
me dijo, 
y me mostró 
que ni siquiera lo intentaría, 
es decir, 
anotarse.

Le dije:

«Ve, anótate, 
y cambia tu sentimiento, 
ten la seguridad
que esta vez 
te lo darán.

¿Si tú no tienes fe?
como alcanzarás lo tuyo?»

sep

El joven fue, 
se anotó 
y me mostró su fe, 
vi que sabía 
que esta vez se lo darían.

sep

Él creyó en la palabra, 
no mentalmente, 
en su alma, 
sabía que ya era acreedor
de ese préstamo.

sep

Un día llega a casa, 
radiante de alegría
y me dice 
que le habían concedido el préstamo
para construir su casa.

sep

Hoy tiene una hermosísima vivienda, 
una hermosa familia, 
y más que eso…

Todos tienen un alma
tan hermosa, 
tan cercana a mí, 
y ya conmigo son.

sep

Mi palabra vuelve a mí
con su propósito hecho, 
y el tiempo 
no es óbice 
si no un camino.

Mi palabra 
es con mi amor, 
con mi paciencia, 
con mi paz, 
con mi misericordia
con mis hijos
que alcanzan mi amor.

sep

El joven 
luego de dos traspiés
comenzó a creer 
a tener fe, 

¿cuántos se habrían dado?
por vencidos?

sep

Hoy tiene la fe
para mover montañas.


