Ser buena persona. 

---

Sé que es difícil 
lo que les pido.

¿Señor que nos pides? 

¿El mundo va? 
en otra dirección? 

---

Entonces, les digo: 
tu palabra te define.

Y tus hechos 
ya lo hicieron.

---

Puedes parecer honesto 
al describirte, 

pero no lo eres, 
eres un alma en pena, 
que aún 
no visualiza su destino.

---

Cuando llegues a él, 
cuando llegues a tu destino,
ahí será tu clamor.

D.

