Vuestras calamidades.

sep

Una tras otra.

Podrían desaparecer
si tan solo se dispusieran
a vivir en paz, 
a amar al prójimo.

Y el prójimo 
no es el otro que me agrada, 
el prójimo es el otro.

Vuestro prójimo
así como ustedes, 
son mis hijos.

sep

Deben amar la paz, 
deben amar la vida, 
no pueden ustedes decir
que confían en mí, 
y disponen quitar la vida.

No tienen autoridad, 
y es un pecado mortal
ante mi.

Deben seguir este camino,
o nada de lo mío los acompañará.

Y lo mío es todo.

sep

No se preguntan por qué.

Porque conocen la respuesta.

sep

¿Por qué?

Es una pregunta 
cuyas respuestas
los llenarán de gozo
o de angustia.

Porque ustedes, 
cada uno, 
son la pregunta y la respuesta.

Son el principio y el fin
de vuestra paz y gozo
de vuestra angustia 
y tristeza sin fin.

sep

Vuestras calamidades, 
son las plagas de la antigüedad.

Son la cosecha a tanto mal
y destrucción, 
y creyéndose omnipotentes
son llenados de humildad
para que observen

cuanto mal hay en vuestras almas, 
cuanto daño han causado, 
y se les ofrece así
la redención.

sep

¡Hijos!
por favor!
sean con mi amor!
sean con mi paz!
sean misericordiosos 
y humildes!

Y todo eso
se les acercará.

La calma retornará a ustedes, 
y si son conmigo, 
mi paz será con ustedes
en vuestras tierras
en vuestros hogares.

Y no pierdan mi paz.

Cuídenla, atesórenla, 
eternamente, 
porque ya conocen
lo que es sin ella.

