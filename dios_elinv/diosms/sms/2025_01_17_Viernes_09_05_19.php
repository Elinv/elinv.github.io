La señal y la marca.

sep

Es igual a propiedad.

El amo, 
pone señal y marca
en lo que es de su propiedad.

El animal
no puede elegir.

sep

Cuando eligen 
ser señalizados, 
ser marcados, 
con una marca indeleble,
con una firma indeleble,
eligen vuestro amo.

Vuestro amo eterno.

sep

Y este pensamiento
esta escritura 
les afirma de mí, 
de quién soy.

sep

Yosoy
y no vengo a ustedes
con mentiras, 
con palabras cómodas,
con silencios cómplices.

sep

El animal no puede elegir
y aun así
aun en su condición
se resiste
a ser señalizado, 
a ser marcado.

sep

Todo les he dicho 
les he alertado.

sep 

Ningún hijo mío
tendrá ninguna señal
ninguna marca ante mí.

Les he dado la libertad
y eso es todo lo vuestro.

sep 

Yo no soy vuestro amo, 
Yosoy vuestro Padre.

Y eso 
no lo podrán cambiar
como no podrán cambiar
mi eterno amor
por mis hijos.



