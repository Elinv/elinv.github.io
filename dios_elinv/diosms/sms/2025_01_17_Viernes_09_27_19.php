El Artista y el Copyright.

sep

No hay entre ustedes
en esta modernidad, 
un solo artista.

Todos pretenden
en una pretención infantil.

sep

Así vuestro tan mal afamado
y mal definido Copyright.

Dicen derechos de autor
y ustedes
ninguno es autor, 
solo van descubriendo
la creación.

sep

La creación 
que les he dado libremente
para que la disfruten, 

ustedes 
contrariando a vuestro Padre, 
y asumiendo su obra, 
le quitan al Padre.

sep

No robarás
les he dicho.

sep

Vuestro Copyright
es una clara afrenta 
y ofensa 
a la obra de vuestro Padre.

sep

Recuérdenlo pronto
cuando estén ante mí.

sep

Cuando lleguen a mí, 
aquellos
que aun en sus almas
tienen un resquicio de mí,

no podrán con la carga
de sus acciones en vida.

sep

Los ayudaré
yo sostendré vuestras cargas, 
y los ayudaré, 
y ustedes caminarán
junto a mí, 
hasta que todo en ustedes
se convierta en mi amor.

sep

Los que no lleguen a mí, 
también serán ayudados.

sep

Se dicen artistas, 
y ni uno lo es.

Un artista 
no existe.

Existe su obra, 
y su obra 
es la exaltación 
de lo noble, del amor, de la paz, 
ver plasmado su alma, 
su espíritu, 
su bondad, 
su elevación 
por sobre el resto, 
para observar el futuro, 
y así guiar 
al resto de mis hijos
por los mejores caminos.

sep

Se dicen artista, 
y vuestra alma
apenas pueden llevar.

Y engañan 
al resto de mis hijos.

sep

¡El artista no existe!

Paradoja 
que les dejo, 
porque aquel 
que ofrezca su obra
solo su obra, 
y se olvide de sí, 
ese será artista ante mí.

Será artista 
en todos mis cielos.

sep

Porque al que tiene
se le dará
y al que no tiene
aun lo que no tiene
se le quitará.

