En mis hijos, dejo…

---

Todo mi poder.

---

En mis hijos, 
ya está el poder 
que Dios Padre 
me ha concedido, 

y ellos 
si alcanzan el amor 
y la paz de Dios, 
de Nuestro Padre, 

podrán ver todo transparente, 

tendrán todo el conocimiento 
en el mismo momento 
que lo requieran, 

serán en conexión con todo, 

tendrán acceso al todo, 

y verán realizados 
todos sus pensamientos.

